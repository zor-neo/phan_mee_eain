Phan Mee Eain Learning Hub is a student learning and sharing platform. Reports help the admin team review content that may reduce learning quality, mislead readers, or break community standards.

Users may report content for reasons such as wrong information, outdated information, missing context, unclear explanation, duplicate content, harassment, or spam.

The admin team will review reports as a demo moderation workflow. A report does not automatically remove content. The final decision belongs to the admin team after checking the reported post and available context.

Repeated reports for the same content are limited by a 24-hour cooldown to reduce spam and unnecessary database activity.

This policy is demo text for the current project version and may be updated when the platform moves closer to deployment.
